package com.iamsee.bean;

public class Menu {
	
	private int id;
	private String menuname;
	private int menusign;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMenuname() {
		return menuname;
	}
	public void setMenuname(String menuname) {
		this.menuname = menuname;
	}
	public int getMenusign() {
		return menusign;
	}
	public void setMenusign(int menusign) {
		this.menusign = menusign;
	}
	
	
	

}
